namespace E3
{
    public class Figurita
    {
        public string nombre, posicion, pais;
        public int numFigurita;

        public Figurita(string nombre, string posicion, string pais, int numFigurita)
        {
            this.nombre=nombre;
            this.posicion=posicion;
            this.pais=pais;
            this.numFigurita=numFigurita;
        }
    }
}